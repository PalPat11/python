def sumOfTwoNumbers(a:float, b:float)->float:
    result:float=None
    result=a+b
    return result

def difOfTwoNumbers(a:float, b:float)->float:
    result:float=None
    result=a-b
    return result

def defOfTwoNumbers(a:float, b:float)->float:
    result:float=None
    result=a/b
    return result

def multOfTwoNumbers(a:float, b:float)->float:
    result:float=None
    result=a*b
    return result

